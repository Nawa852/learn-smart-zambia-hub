import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { body_system, student_diagnosis, phase } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY not configured");

    let systemPrompt = "";
    let userPrompt = "";

    if (phase === "generate") {
      systemPrompt = `You are a clinical case simulator for medical students in Zambia. Generate a realistic patient case scenario. Include: patient demographics (name, age, sex), chief complaint, history of present illness, past medical history, vital signs, physical examination findings. Make it realistic for a Zambian clinical setting. Focus on conditions common in sub-Saharan Africa.`;
      userPrompt = body_system
        ? `Generate a clinical case in the ${body_system} body system category.`
        : `Generate a random clinical case from any body system.`;
    } else if (phase === "evaluate") {
      systemPrompt = `You are a clinical case evaluator. Evaluate the student's diagnosis against the case. Provide: 1) Whether the diagnosis is correct (accuracy_score 0-100), 2) The correct diagnosis if wrong, 3) Key clinical reasoning points they should have considered, 4) Differential diagnoses they should consider. Be educational and encouraging.`;
      userPrompt = `Student's diagnosis: "${student_diagnosis}". Evaluate this diagnosis.`;
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const status = response.status;
      if (status === 429) return new Response(JSON.stringify({ error: "Rate limit exceeded" }), { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } });
      if (status === 402) return new Response(JSON.stringify({ error: "Payment required" }), { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } });
      throw new Error(`AI gateway error: ${status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("medical-case-simulator error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
